    private void andirPosicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_andirPosicionActionPerformed
        MouseControl mouse = new MouseControl();
        JFrame g = this;
        k.setEsCuadrado(false);
        Thread z = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    if (jcEstablecerr.isSelected()) {
                        TransparentBackground.iniciarCaptura();
                        location = true;
                        Robot e = new Robot();
                        while (TransparentBackground.isTeste()) {
                            e.delay(1000);
                        }
                        TransparentBackground.setTeste(true);
                        k.setEsCuadrado(true);
                        k.setCoordenadasClick(TransparentBackground.getCoordenadasPasar());
                        k.setCoordenadas(TransparentBackground.coordenadas);
                        establecerClick1(k, true);
                    } else {
                        Point actualPosi = mouse.devolverPosicion();
                        k.setLocation(actualPosi);
                        establecerClick1(k, true);
                        location = false;
                    }

                } catch (InterruptedException ex) {
                    VentanaNotificaciones.ventanaError("Se ha detenido la ejecuci√≥n", g);
                } catch (Exception ex) {
                    VentanaNotificaciones.ventanaError("Error inesperado" + ex.getMessage(), g);
                }
            }
        }, "AnadirPoss");
        z.start();

    }//GEN-LAST:event_andirPosicionActionPerformed

