    private void jbAceptarCambioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarCambioActionPerformed
        try {
            // TODO add your handling code here:
            if (MouseControl.getConjuntoClicks().isEmpty()) {
                VentanaNotificaciones.ventanaError("No hay clicks a editar", this);
            } else {
                String esco = jcClicks.getSelectedItem() + "";
                for (int i = 0; i < MouseControl.getConjuntoClicks().size(); i++) {
                    if (MouseControl.getConjuntoClicks().get(i).getAlias().equalsIgnoreCase(esco)) {
                        Click used = MouseControl.getConjuntoClicks().get(i);
                        if (location) {
                            used.setCoordenadasClick(k.getCoordenadasClick());
                            used.setCoordenadas(k.getCoordenadas());
                            used.setEsCuadrado(true);
                        } else {
                            if (!used.isEsCuadrado()) {
                                used.setEsCuadrado(false);
                                used.setLocation(new Point((int) Double.parseDouble(jlequisPosicion.getText()), (int) Double.parseDouble(jlyposicion.getText())));

                            }

                        }
                        if (!used.getAlias().equalsIgnoreCase(jtAliasClick.getText()) && !MouseControl.aliasRepetido(jtAliasClick.getText())) {
                            used.setAlias(jtAliasClick.getText());
                        }
                        if (jcUseAlea.isSelected()) {
                            used.setRandom(true);
                            used.setRandomuno(Integer.parseInt(jtAleaUno.getText()) * 1000);
                            used.setRandomdos(Integer.parseInt(jtAleaDos.getText()) * 1000);
                        } else {
                            used.setRandom(false);
                            if (jtSegundosClicks.getText().isEmpty()) {
                                used.setDuracionClick(1600);
                            } else {
                                used.setDuracionClick(Integer.parseInt(jtSegundosClicks.getText()) * 1000);
                            }
                        }
                        if (dobleClick.isSelected()) {
                            used.setDoble(true);
                        } else {
                            used.setDoble(false);
                        }
                    }
                }

                // Cambiar que esto da sida
                DefaultComboBoxModel p = (DefaultComboBoxModel) jcClicks.getModel();
                p.removeAllElements();
                establecerClicks();
                MouseControl.actualizarTabla(tablaClicks);
            }

        } catch (Exception ex) {
            VentanaNotificaciones.ventanaError(ex.getMessage(), this);
        }
    }//GEN-LAST:event_jbAceptarCambioActionPerformed


